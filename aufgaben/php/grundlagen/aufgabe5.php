            Aufgabe 5
        </h5>
        <div class="block">
            <p class="content">
                Geben Sie eine Glückszahl für den Benutzer aus. Die Glückszahl berechnet sich wie folgt:
            </p>
            <ol>
                <li>
                    Wochentag bestimmen (Mon, Tue, ...) und in einer Variablen <code>$wochentag</code> speichern.
                </li>
                <li>Zufallszahl zwischen 1 und 100 in einer Variablen <code>$zufallszahl</code> speichern.</li>
                <li>Die Anzahl der Zeichen im Wochentag mit <code>strlen(STRING)</code> (Abkürzung für string
                    length) bestimmen und in <code>$laenge</code> speichern.
                </li>
                <li>Die Glückszahl ist die Multiplikation (<code>*</code>-Operator) von <code>$laenge</code> und
                    <code>$zufallszahl</code> und wird in der Variablen <code>$luckyNumber</code> gespeichert.
                </li>
            </ol>
            <p class="content">
                Erzeugen Sie dann in der Datei folgende Ausgabe: <br>
                <code>Katja, deine Glückszahl lautet 75.</code>
            </p>
